List_numbers = []
List_squares = []
List_cubes = []
start = 1
end = 10
for element in range(start, end+1):
    List_numbers.append(element)
    List_squares.append(element**2)
    List_cubes.append(element**3)
print("List_numbers: ", List_numbers)
print("List_squares: ", List_squares)
print("List_cubes  : ", List_cubes)



